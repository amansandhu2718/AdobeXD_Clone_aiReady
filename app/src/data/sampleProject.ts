function text(
  id: string,
  name: string,
  content: string,
  x: number,
  y: number,
  width: number,
  fontSize: number,
  color = '#171717',
): DesignElement {
  return {
    id,
    type: 'text',
    name,
    x,
    y,
    width,
    height: fontSize * 1.5,
    rotation: 0,
    opacity: 1,
    locked: false,
    visible: true,
    content,
    style: {
      ...baseStyle,
      fill: 'transparent',
      typography: {
        fontFamily: 'Inter',
        fontSize,
        fontWeight: fontSize > 24 ? 700 : 500,
        lineHeight: 1.25,
        letterSpacing: 0,
        align: 'left',
        color,
      },
    },
  }
}

